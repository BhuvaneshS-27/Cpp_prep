
#include<iostream>
#include<cmath>
using namespace std;
int main(){

    // math functions
    // - min
    // - max
    // - sqrt
    // - pow
    // - round 
    // - floor
    // - ceil
    // - log

    int n1=5, n2=7;
    cout<<max(n1,n2)<<endl;
    cout<<min(n1,n2)<<endl;

    //Below uses <cmath> header file 
    cout<<sqrt(2)<<endl;
    cout<<pow(7,3)<<endl;
    cout<<round(5.4)<<endl;
    cout<<ceil(5.4)<<endl;
    cout<<floor(5.6)<<endl;
    cout<<log(2)<<endl;
}